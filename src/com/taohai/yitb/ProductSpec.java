package com.taohai.yitb;

public class ProductSpec {

	public String ProColorName;
	
	public String ProSizesName;
	
	public String ProSkuNo;
	
	public String getProColorName() {
		return ProColorName;
	}

	public void setProColorName(String proColorName) {
		ProColorName = proColorName;
	}

	public String getProSizesName() {
		return ProSizesName;
	}

	public void setProSizesName(String proSizesName) {
		ProSizesName = proSizesName;
	}

	public String getProSkuNo() {
		return ProSkuNo;
	}

	public void setProSkuNo(String proSkuNo) {
		ProSkuNo = proSkuNo;
	}

	public String getWeight() {
		return Weight;
	}

	public void setWeight(String weight) {
		Weight = weight;
	}

	public String Weight;
}
